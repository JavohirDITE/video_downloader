async function downloadVideo(url, outputPath, quality = "720") {
  let formatSelector

  switch (quality) {
    case "1080":
      formatSelector = "best[height<=1080]/best"
      break
    case "720":
      formatSelector = "best[height<=720]/best"
      break
    case "480":
      formatSelector = "best[height<=480]/best"
      break
    case "360":
      formatSelector = "best[height<=360]/best"
      break
    default:
      formatSelector = "best[height<=720]/best"
  }

  const ytDlpOptions = [
    "--no-playlist",
    `--format "${formatSelector}"`,
    '--user-agent "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36"',
    '--referer "https://www.youtube.com/"',
    '--add-header "Accept-Language:en-US,en;q=0.9"',
    '--add-header "Accept-Encoding:gzip, deflate"',
    '--add-header "Accept:text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8"',
    '--add-header "Connection:keep-alive"',
    '--add-header "Upgrade-Insecure-Requests:1"',
    "--extractor-retries 3",
    "--fragment-retries 3",
    "--retry-sleep 1",
    "--no-check-certificate",
    "--prefer-free-formats",
    "--youtube-skip-dash-manifest",
  ].join(" ")

  const command = `yt-dlp ${ytDlpOptions} -o "${outputPath}" "${url}"`
  console.log(`–í—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è –∫–æ–º–∞–Ω–¥–∞: yt-dlp —Å –∫–∞—á–µ—Å—Ç–≤–æ–º ${quality}p`)

  try {
    const { stdout, stderr } = await execPromise(command, { timeout: 300000 })
    console.log("yt-dlp stdout:", stdout)
    if (stderr) console.log("yt-dlp stderr:", stderr)
    return true
  } catch (error) {
    console.error("–û—à–∏–±–∫–∞ yt-dlp:", error)

    // Fallback –¥–ª—è –ø—Ä–æ–±–ª–µ–º–Ω—ã—Ö –≤–∏–¥–µ–æ
    console.log("–ü—Ä–æ–±—É–µ–º –∞–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–Ω—ã–π –º–µ—Ç–æ–¥ —Å–∫–∞—á–∏–≤–∞–Ω–∏—è...")
    const fallbackCommand = `yt-dlp --no-playlist --format "worst[height<=480]/worst" --user-agent "Mozilla/5.0 (iPhone; CPU iPhone OS 14_0 like Mac OS X)" -o "${outputPath}" "${url}"`

    try {
      const { stdout, stderr } = await execPromise(fallbackCommand, { timeout: 300000 })
      console.log("–ê–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–Ω—ã–π –º–µ—Ç–æ–¥ —É—Å–ø–µ—à–µ–Ω:", stdout)
      return true
    } catch (fallbackError) {
      console.error("–ê–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–Ω—ã–π –º–µ—Ç–æ–¥ —Ç–∞–∫–∂–µ –Ω–µ —Å—Ä–∞–±–æ—Ç–∞–ª:", fallbackError)
      throw error
    }
  }
}

